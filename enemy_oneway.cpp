#include <assert.h>
#include "enemy_oneway.h"

//-----------------------------------------------------------------------------
// 定数
//-----------------------------------------------------------------------------
const D3DXVECTOR3 CEnemyOneWay::SCALE = D3DXVECTOR3(0.5f, 0.75f, 0.5f);
const D3DXVECTOR3 CEnemyOneWay::MOVE_POWER = D3DXVECTOR3(0.0f, -1.5f, 0.0f);

//-----------------------------------------------------------------------------
// コンストラクタ
//-----------------------------------------------------------------------------
CEnemyOneWay::CEnemyOneWay()
{
	SetType(CObject::EType::PLAYER);
}

//-----------------------------------------------------------------------------
// デストラクタ
//-----------------------------------------------------------------------------
CEnemyOneWay::~CEnemyOneWay()
{
}

//-----------------------------------------------------------------------------
// 初期化
//-----------------------------------------------------------------------------
HRESULT CEnemyOneWay::Init()
{
	// 現在のモーション番号の保管
	CEnemy::Init();
	LoadModel("BOX");
	SetScale(SCALE);
	SetMove(MOVE_POWER);
	return S_OK;
}

//-----------------------------------------------------------------------------
// 終了
//-----------------------------------------------------------------------------
void CEnemyOneWay::Uninit()
{
	CEnemy::Uninit();
}

//-----------------------------------------------------------------------------
// 更新
//-----------------------------------------------------------------------------
void CEnemyOneWay::NormalUpdate()
{
	CEnemy::NormalUpdate();
}

